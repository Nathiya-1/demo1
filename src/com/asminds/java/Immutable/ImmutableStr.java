package com.asminds.java.Immutable;

public final class ImmutableStr {
	final String name;
	final int regno;
	
	public ImmutableStr(String name, int regno) {
		super();
		this.name = name;
		this.regno = regno;
	}
	public String getName() {
		return name;
	}
	public int getRegno() {
		return regno;
	}
	
	
	
	
	
	
}

